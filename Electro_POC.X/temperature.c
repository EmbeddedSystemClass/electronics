#define OW_bus(state) LATBbits.LATB10 = state

void	init_my_delay(void)
{
    	/*TIMER4 (micro sec measurement)*/
	T5CONbits.ON    = 0x00;		//Disable
	T5CONbits.TCKPS = 0x00;		//1:1
    IEC0bits.T5IE = 0;
}

void    my_delay(int us)
{
    int i;
    i = 0;
    
    TMR5 = 0;
    T5CONbits.ON = 1;
    while (i < us)
    {
        while (TMR5 < 10);
        TMR5 = 0;
        i++;
    }
    T5CONbits.ON = 0;
    IEC0bits.T5IE = 1;
}

void	init_temp()
{
    TRISBbits.TRISB10 = 0; // output
    OW_bus(1); //define OW high
    ODCBbits.ODCB10 = 1; //enable open drain on RB10
    display_write('T', 0, 15);
}

int	reset()
{
    int secure;
    
    secure = 0;
    Presence = 0;
    while (Presence == 0 && secure < 4)
    {
        //drive bus low for 480us
        OW_bus(0); //OW low (open drain)
        my_delay(480); // 480us (reset pulse)
        OW_bus(1); // release OW
        my_delay(80); //wait 70-160us
         //sample bus
        if (PORTBbits.RB10 == 0) //presence pulse
        {
            return (1);
        }
        else
        {
            Presence = 0;
            secure++;
        }
        my_delay(400);
    }
    display_write_str("----", 0, 16);
    display_update();
    return (Presence);
}

void    WriteBit(uint8_t bit)
{
    TRISBbits.TRISB10 = 0; //pin output
    if (bit == 1)
    {
        //write 1
        OW_bus(0); //drive OW low
        my_delay(1);
        OW_bus(1); //release OW
        my_delay(60);
    }
    else
    {
        OW_bus(0); //drive OW low
        my_delay(61);
        OW_bus(1); //release OW
    }
}

void    WriteData(uint8_t data)
{
    int i = 0;
    uint8_t  bitmask = 0x01;
    
    while (i < 8)
    {
        if ((bitmask & data) > 0)
        {
            WriteBit(1);
        }
        else
        {
            WriteBit(0);
        }
        bitmask = bitmask << 1;
        i++;
    }
    
}

uint8_t    Readbit()
{
    uint8_t i;
    
    OW_bus(0); //drive OW low
    //while (TMR1 < 10);
    my_delay(1);
    OW_bus(1);
    TRISBbits.TRISB10 = 1; //set input
   // while (TMR1 < 100)
    //{}
    my_delay(10);
    if (PORTBbits.RB10 == 0)
    {
        i = 0;
    }
    else
    {
        i = 1;
    }
    my_delay(49);
    //OW_bus(1);
    TRISBbits.TRISB10 = 0; // pin output
    return i;
}

uint8_t ReadData()
{
    uint8_t i = 0;
    uint8_t data = 0x00;
    uint8_t bitmask = 0x01;
    
    while (i < 8)
    {
        if (Readbit() == 1)
        {
            data = data | bitmask;
        }
        bitmask = bitmask << 1;
        i++;
    }
    return (data);
}

void    skipROM()
{
    WriteData(0xCC); //skip ROM
}

uint8_t    convertTmp()
{
    uint8_t s;
    
    s = 0;
    if (reset() == 0)
    {
        return(0);
    }
    skipROM();
    WriteData(0x44); //convert temperature (96ms))
    //Securite erreur
    IEC0bits.T4IE = 0;
    TMR4 = 0;
    T4CONbits.ON = 1;
    while (Readbit() == 0) // CF datasheet en mode normale, le capteur retourne 0 sur le OW si operation en cou, et 1 une foisla convertion fini
    {
        if (TMR4 > 10000000) //1s
        {
            T4CONbits.ON = 0;
            TMR4 = 0;
            IEC0bits.T4IE = 1;
            display_write_str("----", 0, 16);
            return (0);
        }
    }
    T4CONbits.ON = 0;
    TMR4 = 0;
    IEC0bits.T4IE = 1;
}
    
void    SampleTmp()
{
    if (reset() == 0)
    {
        return;
    }
    skipROM();
    WriteData(0xBE); //read scratchpad command
    LSB = ReadData();
    MSB = ReadData();
}

void    readrom()
{
    while (reset() == 0);
    WriteData(0x33);
    LSB = ReadData();
    MSB = ReadData();
}

void    read_power_supply()
{
    while (reset() == 0);
    skipROM();
    WriteData(0xB4);
}

void    copy_to_eeprom()
{
    while (reset() == 0);
    skipROM();
    WriteData(48);
}

void    write_scratchpad()
{
    while (reset() == 0);
    skipROM();
    WriteData(0x4E); //set config
    // Envois des 3 octets
    WriteData(0xFF); //Th
    WriteData(0x00); //Tl
    WriteData(0b00011111); //Resolution 9bits
    // Copy Scratchpad to EEPROM
    copy_to_eeprom();
}

void    check_temp()
{
    uint8_t is_neg = 0b11111000;
    
    init_my_delay();
    write_scratchpad(); //Define resolution
    convertTmp(); //save Temperature value
    SampleTmp(); // get Temperature value
    Temperature = (((MSB & 0b00000111) << 8) | LSB & 0b11111000) * 0.0625; //converstion en °c
    if ((MSB & is_neg) == is_neg) //check negative value
    {
        Temperature = Temperature * -1;
    }
    display_write_str("    ", 0, 16);
    display_write_dec(Temperature, 0, 16);
    display_update();
}